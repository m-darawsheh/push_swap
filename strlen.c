#include "push_swap.h"

size_t	my_strlen(char *str)
{
	size_t	i;
	
	i = 0;
	if (str[i] == '-' || str[i] == '+')
		i++;
	while (str[i] != '\0')
		i++;
	return (i - 1);
}
