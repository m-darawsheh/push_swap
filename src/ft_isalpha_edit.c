#include "../include/push_swap.h"

int	ft_isalpha_edit(char *c)
{
	int	i;

	i = 0;
	if (c[i] == '-' || c[i] == '+')
		i++;
	while (c[i])
	{
		if (c[i] > '9' || c[i] < '0')
			return (1);
		i++;
	}
	return (0);
}
